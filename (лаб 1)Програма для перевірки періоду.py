#Числа з варіанту 14
M = 8388607
A = 1000
C = 377
X0 = 7


def linear_congruential_generator(Xn, a, c, m):
    """
    Та сама функція, що реалізує формулу:
    Xn+1 = (a * Xn + c) % m
    """
    return (a * Xn + c) % m


print("--- Початок перевірки періоду ---")
print(f"Параметри: m={M}, a={A}, c={C}, X0={X0}")

# Генеруємо перше число (X1) на основі початкового X0
next_X = linear_congruential_generator(X0, A, C, M)

# Починаємо рахувати період. Оскільки ми вже згенерували
# одне число (X1), наш лічильник починається з 1
period = 1

# 'while next_X != X0:' — це цикл, який буде працювати
# нескінченно, ДОКИ згенероване число 'next_X'
# не стане рівним нашому початковому числу 'X0'
while next_X != X0:
    
    # Генеруємо наступне число в послідовності
    next_X = linear_congruential_generator(next_X, A, C, M)
    
    # Збільшуємо лічильник періоду на 1
    period += 1
    
    # Запобіжник
    # Якщо з якоїсь причини (помилка в логіці або параметрах)
    # період стане довшим за модуль, ми зупинимо цикл,
    # щоб уникнути нескінченної роботи
    if period > M:
        print(f"Помилка: Період ({period}) перевищив модуль ({M}). Алгоритм зупинено.")
        period = -1 # Встановлюємо "помилкове" значення
        break # Негайно виходимо з циклу 'while'

# Коли цикл 'while' завершиться (або через 'break', або штатно),
# ми перевіряємо, чи не спрацював наш запобіжник
if period != -1:
    print(f"\n--- Перевірку завершено ---")
    print(f"Період функції генерації: {period}")